    private void atuaizar_labels_index_info(){
        this.label_index_extacionamento.setText("Estacionamento: "+Tela.controler.getEstacionamento_index());
        String bancos_String = "";
        Iterator it = Tela.controler.getBancos().iterator();
        while(it.hasNext()){
            bancos_String+= it.next() ;
            bancos_String+=" ";
        }
        System.out.println("Bancos:" + bancos_String );
        this.label_lista_index_bancos.setText("Bancos:" + bancos_String );
        String coletas_String = "";
        it = Tela.controler.getColetas().iterator();
        while(it.hasNext()){
            coletas_String+=it.next();
            coletas_String+=" ";
        }
        System.out.println("Coleta: "+ coletas_String);
        this.label_lista_index_coletas.setText("Coleta: "+ coletas_String);
        this.label_num_vertices.setText("Numero de vertices no grafo: "+ Tela.controler.getGrafo().getNumVertices() );
        // Essa parte do codigo atualiza os labels de informaçao da tela do dijkstra
        this.label_index_extacionamento_dijskstra.setText("Estacionamento: "+Tela.controler.getEstacionamento_index());
        this.label_lista_index_bancos_dijskstra.setText("Bancos:" + bancos_String );
        this.label_lista_index_coletas_dijskstra.setText("Coleta: "+ coletas_String);
        this.label_num_vertices_dijskstra.setText("Numero de vertices no grafo: "+ Tela.controler.getGrafo().getNumVertices() );
        
    }
   
