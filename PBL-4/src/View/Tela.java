    private void buton_atualizar_arestaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buton_atualizar_arestaActionPerformed
        // TODO add your handling code here:
        int index1 = 0;
        int index2 = 0;
        int valor = 0;
        try{
            index1 = Integer.parseUnsignedInt( this.text_aresta_index1.getText() );
            index2 = Integer.parseUnsignedInt( this.text_aresta_index2.getText() );
            if( !(this.text_aresta_valor.getText() == "") )
            valor = Integer.parseUnsignedInt( this.text_aresta_valor.getText() );
        }catch(NumberFormatException e){
            ERRO.dialogErro("Erro Numero nao digitado", "Porfavor coloque numeros nao negativos nos campos");
            return;
        }
        if( index1 == index2 ){
            ERRO.dialogErro("Erro no swap", "Dgite numeros nao iguais por favor");
        }else if( index1 < 0 || index1 > Tela.controler.getGrafo().getNumVertices()  ){
            ERRO.dialogErro("Erro no swap index 1", "O numero no index 1 é menor que zero ou maior que o numero de vertices do grafo");
        }else{
            Tela.controler.editarAresta(index1, index2, valor);
            ERRO.dialogErro("SUCESSO", "A ligação de "+ index1+" e "+index2 + " foi adicionada com sucesso");
        }
    }//GEN-LAST:event_buton_atualizar_arestaActionPerformed

